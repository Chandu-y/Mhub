languages.forEach(lang => {
    const filePath = path.join(localesDir, `${lang}.json`);
    let content = {};

    try { content = JSON.parse(fs.readFileSync(filePath, 'utf8')); } catch (e) { }

    let updated = 0;
    Object.keys(dictionary).forEach(key => {
        // If specific language translation exists in dictionary
        if (dictionary[key][lang]) {
            // If current value is missing OR is placeholder (same as English/Key/starts with +)
            const current = content[key];
            const isPlaceholder = !current || current === key || current.startsWith('+') || /^[A-Za-z0-9_ ]+$/.test(current) && current.length > 3 && current === key;

            // Also check if current value is exactly the English translation (which implies it wasn't translated)
            // We skip this check to be safe, assuming our dictionary is better.
            // But we should prioritize existing translation if it looks native?
            // Native check: does it contain non-ascii?
            const isNative = current && /[^\u0000-\u007F]/.test(current);

            if (!current || !isNative) { // If missing or English-looking
                content[key] = dictionary[key][lang];
                updated++;
            }
        }
    });

    if (updated > 0) {
        const sorted = {};
        Object.keys(content).sort().forEach(k => sorted[k] = content[k]);
        fs.writeFileSync(filePath, JSON.stringify(sorted, null, 2));
        console.log(`Updated ${lang}.json: ${updated} keys`);
    } else {
        console.log(`${lang}.json up to date.`);
    }
});
